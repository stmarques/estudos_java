package exericiosjava;

public class DeclaracaoVariaveis {

	public static void main(String[] args) {
		//DECLARAÇÃO SIMPLES
		int idade1 = 3; //inteiro
		float salario1 = 1825.54f; //real
		char letra1 = 'G'; //literal
		boolean casado1 = false; //lógico
		
		//TYPE CAST
		int idade2 = (int)3; //inteiro
		float salario2 = (float)1825.54; //real
		char letra2 = (char) 'G'; //literal
		boolean casado2 = (boolean) false; //lógico
		
		//WRAPPER CLASS (CLASSE INVÓLUCRO)
		Integer idade3 = new Integer(3); //inteiro
		Float salario3 = new Float (1825.54); //real
		Character letra3 = new Character ('G'); //literal
		Boolean casado3 = new Boolean(false); //lógico
		
		

	}

}
